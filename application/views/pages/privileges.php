        <p><a name="2.1"></a><strong>1. WHO ARE ENTITLED TO DUTY AND TAX FREE PRIVILEGES?</strong></p>
        <p>Section 105 of the Tariff and Customs Code of the Philippines as amended by Executive Order No. 206 provides duty and tax free privileges to the following individuals, the extent of which depends on their particular status:</p>
        <ol>
        <li>Returning Resident. A Returning Resident is a Filipino national who has gone abroad and is now returning. Only those Returning Residents who have an uninterrupted stay abroad for at least six (6) months prior to their return to the Philippines are entitled to duty and tax free privileges.</li>
        <li>Overseas Filipino Worker (OFW) is a Filipino national who worked in a foreign country under an employment contract. Only OFWs who have an uninterrupted stay abroad for more than six (6) months are entitled to duty and tax free privileges.</li>
        <li>Former Filipino. A Filipino national who has acquired foreign citizenship abroad and is now returning. Only former Filipinos who are coming to settle permanently in the Philippines and have stayed abroad for at least six months are entitled to the duty and tax exemption privileges.</li>
        </ol>
        <p><a name="2.2"></a><strong>2. ARE BALIKBAYANS ALSO ENTITLED TO CERTAIN DUTY AND TAX FREE PRIVILEGES?</strong></p>
        <p>BALIKBAYAN is another term for an OFW or a former Filipino. A returning resident is also a BALIKBAYAN when he has stayed abroad for at least one year. Balikbayans are entitled to duty and tax free privileges.</p>
        <p><a name="2.3"></a><strong>3. WHAT IS THE EXTENT OF DUTY AND TAX FREE PRIVILEGE OF RETURNING FILIPINOS/BALIKBAYANS?</strong></p>
        <p>The extent varies as follows:</p>
        <ol>
        <li>Returning Resident. Personal effects and household goods used by him abroad for at least six (6) months and the dutiable value of which is not more than Ten Thousand Pesos (10,000.00) are exempt from duties and taxes. Any amount in excess of P10,000.00 is subject to 50% duty to the first P10,000.00 exemption across the board as provided for under Section 105 (F) of the TCCP.</li>
        <li>Overseas Filipino Worker (OFW). In addition to the privileges granted to Returning Residents as described above, an OFW may be allowed to bring in, duty and tax free Ten Thousand (P10,000.00) of USED home appliances, provided:<br />
        the quantity is limited to one of each kind;</p>
        <ol>
        <li>the privilege has not been enjoyed previously during the calendar year which fact must be declared under oath by the owner;</li>
        <li>the owner&#8217;s passport is presented at the port/airport of entry;</li>
        <li>any amount in excess of P10,000.00 will be subject to duty and tax.</li>
        </ol>
        </li>
        <li>Former Filipino. Professional instruments and implements, tools of trade occupation or employment, wearing apparel, domestic animals, and personal household effects shall be exempt from payment of Customs duties and taxes. Provided that the change of residence was bonafide, the privilege of free entry was never availed of before and that the articles are brought from their former place of abode.</li>
        </ol>
        <p><a name="2.4"></a><strong>4. ARE FAMILY MEMBERS OF RETURNING RESIDENTS, OFWs AND FORMER FILIPINOS ALSO ENTITLED TO CERTAIN PREVILEGES?</strong></p>
        <p>Yes, provided they themselves satisfy FAQs Chapter 1&#8242;s first three points</p>
        <p><a name="2.5"></a><strong>5. WHAT ARE THE CONDITIONS AND LIMITATIONS ATTACHED TO TAX EXEMPTION PRIVILEGES?</strong></p>
        <p>The following are the conditions for availment of duty and tax privileges;</p>
        <ol>
        <li>Presentation to the Bureau of Customs of a favorable written endorsement which department controls the availment of duty and tax free exemptions.</li>
        <li>The goods extended duty and tax free privilege are not to be sold, bartered nor traded.</li>
        <li>The quantity is not commercial.</li>
        <li>The goods are not prohibited importations. <em>(See <a href="http://customs.gov.ph/faqs/prohibited-restricted-importations/">Chapter VI. Prohibited/Restricted Importations</a>)</em></li>
        <li>For regulated items, endorsements from the proper regulatory agency. <em>(See <a href="http://customs.gov.ph/faqs/pet-animals-and-household-plants/">Chapter V. Pet/Animals &amp; Household Plants</a>)</em></li>
        </ol>
