        <p><a name="1.1"></a><strong>1. WHAT ARTICLES ARE SUBJECT TO DUTY?</strong></p>
        <p>All articles, when imported to the Philippines, are subject to duty upon each importation, even though previously exported there except as otherwise specifically provided for in the Tariff and Customs Code, as amended, or in other laws.</p>
        <p><a name="1.2"></a><strong>2. WHEN DOES IMPORTATION BEGIN AND DEEMED TERMINATED?</strong></p>
        <p>Importation begins when the carrying vessel or aircraft enters the jurisdiction of the Philippines with the intention to unlade therein. Importation is deemed terminated upon payment of the duties, taxes and other charges due upon the articles, or secured to be paid, at a port of entry and the legal permit for withdrawal shall have been granted, or in case said articles are free of duties, taxes and other charges, until they have legally left the jurisdiction of Customs.</p>
        <p><a name="1.3"></a><strong>3. WHO ARE AUTHORIZED TO MAKE IMPORT ENTRY?</strong></p>
        <p>The following are authorized to make import entry:</p>
        <ol>
        <li>Importers and other persons who are holders of the bill of lading.</li>
        <li>Licensed Customs broker acting under authority from a holder of the bill.</li>
        <li>A person duly empowered to act as agent or attorney-in-fact for each other.</li>
        </ol>
        <p><em>(The duly notarized power of attorney should be approved by the Port Collector and no more than such continuing power may be accepted or recognized)</em></p>
        <p><a name="1.4"></a><strong>4. WHEN TO FILE AN ENTRY?</strong></p>
        <p>Entry must be filed in the Customhouse within 30 days from the date of discharge of the last package from the vessel, which shall not be extendible. Failure to file the entry constitutes implied abandonment and will result in the <em>&#8216;ipso facto&#8217;</em> forfeiture of the goods/shipment.</p>
        <p><a name="1.5"></a><strong>5. WHAT ARTICLES ARE SUBJECT TO DUTY?</strong></p>
        <p>There are two kinds of import entry, to wit: Formal and the Informal Entry.</p>
        <p>Informal Entry</p>
        <ul>
        <li>Articles of a commercial nature intended for sale, barter or hire, the dutiable value of which does not exceed P2,000.00</li>
        <li>Personal and household effects or articles, not in commercial quantity, imported in passenger&#8217;s baggage, mail, or otherwise for personal use.</li>
        </ul>
        <p>Formal Entry</p>
        <ul>
        <li>Articles of a commercial nature intended for sale, barter, or hire, the dutiable value of which is more than P2,000.00</li>
        <li>Articles for, which the Collector may, upon the recommendation of the Tariff Commission for the protection of a local industry, or the revenue, require formal entry regardless of value and whatever purpose and nature of the importation.</li>
        </ul>
